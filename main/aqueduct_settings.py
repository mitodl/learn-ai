    # ===== model_validators: derive complex settings from primitives =====
    # LOGGING is deliberately absent from this model: main.settings imports
    # it directly from mitol.observability.settings.logging rather than
    # building it, and the settings shim modules do the same.

    @model_validator(mode="after")
    def _configure_database(self) -> AqueductSettings:
        """Build DATABASES/DEFAULT_DATABASE_CONFIG from DATABASE_URL, mirroring
        the dj_database_url.parse(...) + CONN_MAX_AGE + SSL OPTIONS logic in
        main.settings.
        """
        config = dj_database_url.parse(self.DATABASE_URL)
        config["CONN_MAX_AGE"] = self.MITOL_DB_CONN_MAX_AGE
        config["OPTIONS"] = {} if self.MITOL_DB_DISABLE_SSL else {"sslmode": "require"}
        self.DEFAULT_DATABASE_CONFIG = config
        self.DATABASES = {"default": config}
        return self

    @model_validator(mode="after")
    def _configure_static_url(self) -> AqueductSettings:
        """Rewrite STATIC_URL to point at CLOUDFRONT_DIST when it is set."""
        if self.CLOUDFRONT_DIST:
            self.STATIC_URL = urljoin(
                f"https://{self.CLOUDFRONT_DIST}.cloudfront.net", self.STATIC_URL
            )
        return self

    @model_validator(mode="after")
    def _configure_conditional_apps(self) -> AqueductSettings:
        """Extend MIDDLEWARE/INSTALLED_APPS based on feature-flag and DEBUG env vars."""
        middleware = self.MIDDLEWARE
        installed_apps = self.INSTALLED_APPS
        if self.MIDDLEWARE_FEATURE_FLAG_QS_PREFIX:
            middleware = (
                *middleware,
                "main.middleware.feature_flags.QueryStringFeatureFlagMiddleware",
                "main.middleware.feature_flags.CookieFeatureFlagMiddleware",
            )
        if self.DEBUG:
            installed_apps = (*installed_apps, "debug_toolbar")
            middleware = (
                "debug_toolbar.middleware.DebugToolbarMiddleware",
                *middleware,
            )
        self.MIDDLEWARE = middleware
        self.INSTALLED_APPS = installed_apps
        return self

