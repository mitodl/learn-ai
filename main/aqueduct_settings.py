class DevAqueductSettings(AqueductSettings):
    """Local-dev variant: fills missing values from Vault via OIDC instead of
    requiring a .env file.

    Env vars still take priority over Vault when explicitly set, so a developer
    can still override individual values locally.
    """

    @classmethod
    def settings_customise_sources(
        cls,
        settings_cls,
        init_settings,
        env_settings,
        dotenv_settings,  # noqa: ARG003
        file_secret_settings,  # noqa: ARG003
    ):
        """Prepend a Vault OIDC source, keeping init/env values authoritative."""
        import os

        from django_aqueduct.sources.vault import VaultSettingsSource

        # VAULT_AQUEDUCT_PATH and the "learn-ai" role default are
        # placeholders. The real values depend on how secrets are laid out
        # under the secret-learn-ai Vault mount today, which isn't
        # discoverable from this repo -- confirm against the live Vault
        # instance (e.g. `vault kv list secret-learn-ai/`) and set
        # VAULT_AQUEDUCT_PATH / VAULT_AQUEDUCT_ROLE accordingly, or hardcode
        # them here once confirmed.
        return (
            init_settings,
            env_settings,
            VaultSettingsSource(
                settings_cls,
                vault_url=os.environ["VAULT_ADDR"],
                vault_path=os.environ.get("VAULT_AQUEDUCT_PATH", ""),
                mount_point="secret-learn-ai",
                kv_version="2",
                auth_method="oidc",
                role=os.environ.get("VAULT_AQUEDUCT_ROLE", "learn-ai"),
            ),
        )
